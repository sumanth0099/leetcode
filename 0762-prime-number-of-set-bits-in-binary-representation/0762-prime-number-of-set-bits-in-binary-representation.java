class Solution {
    public int countPrimeSetBits(int left, int right) {
        int c=0;
        for(int i=left;i<=right;i++){
            int count=Integer.bitCount(i);
            if(count==2||count==3||count==5||count==7||count==11||count==13||count==17||count==19){
                c++;
            }
            
        }
        return c;
    }
}